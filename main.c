// Discrete sequence generation
int DutyCycle[SEQUENCE_LEN]={20,30,40,50,60}; //impulsais
int tick=0;

void DiscreteSequence(void)
{
	static unsigned char PeriodNo=0;
	
	tick++;  // padidinti impulsu (milisekundžiu) skaitikli
	
	if (tick==PERIOD-1)
	{	GPIOOn();
		tick=0;
		
		if (PeriodNo==SEQUENCE_LEN-1)
		   PeriodNo=0;
		else
			PeriodNo++;
	}
	else if (tick==DutyCycle[PeriodNo]-1)
		  GPIOOff();
		
}

